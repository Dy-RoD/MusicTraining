    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed
        // TODO add your handling code here:
        String query,name,author,addList;
        int id,count=0;
        connect=new Connect();
        link=connect.ConnectToDB();
        DefaultListModel model=new DefaultListModel();
        Songs.setModel(model);
        if(Author)
            query="select * from Song where Author= '"+Text.getText()+"'";
        else
            query="select * from Song where Name= '"+Text.getText()+"'";
        try{
            Statement s=link.createStatement();
            ResultSet rs=s.executeQuery(query);
            while(rs.next()){
                count++;
                id=rs.getInt("idSong");
                name=rs.getString("Name");
                author=rs.getString("Author");
                addList=author+"  -  "+name;
                SongList.add(id);
                model.addElement(addList);
            }
            if(count==0){
                JOptionPane.showMessageDialog(null, "No se encontro ninguna cancion o autor");
                Text.setText("");
                model.removeAllElements();
            }
            else{
                add.setVisible(true);
              add.setEnabled(true);
            }
            connect.CloseConection();
        }catch(SQLException e){
            Logger.getLogger(Connect.class.getName()).log(Level.SEVERE, null, e);
            connect.CloseConection();
        }
    }//GEN-LAST:event_SearchActionPerformed

