    public ViewPlayList(int id) {
        initComponents();
        int count=0;
        this.setLocationRelativeTo(null);
        this.setTitle("My Playlist");
        DefaultListModel model=new DefaultListModel();
        View.setModel(model);
        Id=id;
        connect=new Connect();
        link=connect.ConnectToDB();
        try{
            Statement s=link.createStatement();
            query = "SELECT * FROM Song JOIN UserSongs ON Song.idSong = UserSongs.idSong WHERE UserSongs.idUser = '" + Id + "'";
            ResultSet rs=s.executeQuery(query);
            while(rs.next()){
                count++;
                int idSong=rs.getInt("idSong");
                String songName=rs.getString("Name");
                String author=rs.getString("Author");
                model.addElement(songName+" - "+author+".");
            }
            
            connect.CloseConection();
        }catch(SQLException e){
            Logger.getLogger(Connect.class.getName()).log(Level.SEVERE, null, e);
            connect.CloseConection();
        }
        if(model.isEmpty()){
            JOptionPane.showMessageDialog(null, "Error: No Tienes Canciones AÃ±adidas");
            this.setVisible(false);
            this.dispose();
                
                
        }
    }
    
