    private void ConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfirmActionPerformed
        User user=new User();
        Users users=new Users();
        connect=new Connect();
        link=connect.ConnectToDB();       
        user=users.SearchUser(link, User.getText(), Mail.getText());
        if(user!=null){
            //User.getText(),Pass.getText(),Mail.getText()
            boolean delete=users.Delete(user.getId());
            if(delete){
                JOptionPane.showMessageDialog(null, "Cuenta Eliminada!");
                connect.CloseConection();
                Settings.dispose();
                App.dispose();
                Login login=new Login();
                login.setVisible(true);
                this.dispose();
            }
            else{
                JOptionPane.showMessageDialog(null, "Error: Ingrese Los Datos Correctamente");
                connect.CloseConection();
                User.setText("");
                Pass.setText("");
                Mail.setText("");                
            }
        }
        else{
           JOptionPane.showMessageDialog(null, "Error: Usuario No Encontrado");
           connect.CloseConection();
           User.setText("");
           Pass.setText("");
           Mail.setText("");            
        }

        //Sistema();
    }//GEN-LAST:event_ConfirmActionPerformed

